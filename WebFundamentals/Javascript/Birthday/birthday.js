var daysUntilMyBirthday = 60;

for(var daysUntilMyBirthday = 60; daysUntilMyBirthday > 0; daysUntilMyBirthday--)
{
    if(daysUntilMyBirthday >= 30){
        console.log(daysUntilMyBirthday + ' days until my birthday. Such a long time... :(\n');
    }
    else if ((daysUntilMyBirthday < 30) && (daysUntilMyBirthday >= 5)) {
        console.log(daysUntilMyBirthday + ' days until my bday yayyy!\n');
    }
    else {
        console.log(daysUntilMyBirthday + ' DAYS UNTIL MY BIRTHDAY!!!\n');
    }
}

